  /**
   * Finds a blog by name
   * @param {*} blogName 
   * @returns 
   */
  static findBlog(blogName) {
    const defaultBlog = 'afanti2';
    let blog = AllPosts.ALL_POSTS.find(item => item.blogName == blogName);
    if (!blog) {
      blog = AllPosts.ALL_POSTS.find(item => item.blogName == defaultBlog);
    }
    return blog;
  }

  /**
   * Finds a category from a blog by name
   * @param {*} blog 
   * @param {*} categoryName 
   * @returns 
   */
  static findCategory(blog, categoryName) {
    let category = blog.categories.find(item => item.name == categoryName);
    if (!category) {
      category = blog.categories[0];
    }
    return category;
  }

