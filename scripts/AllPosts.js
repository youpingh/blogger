  static getBlogNames() {
    return AllPosts.ALL_POSTS.map(blog => {
      return blog.blogName;
    });
  }

  static getCategoryNames(blogName) {
    const blog = AllPosts.findBlog(blogName);
    return blog.categories.map(function (category) {
      return category.name;
    });
  }

  static getPostNames(blogName, categoryName) {
    const blog = AllPosts.findBlog(blogName);
    const category = AllPosts.findCategory(blog, categoryName);
    return category.posts.map(post => {
      return post.title;
    });
  }

